import java.awt.*;

public class ConstantRunner {




    public void printColor (String color) {
        String aColor= color;

        switch (aColor){

            case ConstantContainer.RED:
                System.out.println(ConstantContainer.RED);
                break;
                case ConstantContainer.GREEN:
                System.out.println(ConstantContainer.GREEN);
                break;
        }





    }
}
